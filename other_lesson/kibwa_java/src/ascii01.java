public class ascii01 {
    public static void main(String[] args) {
        System.out.println("개행: "+(int)'\n');
        System.out.println("스페이스(공백): "+(int)' ');
        System.out.println("null: "+(int)'\0');
        System.out.println("a: "+(int)'a');
        System.out.println("A: "+(int)'A');
        System.out.println("0: "+(int)'0');

    }
}
